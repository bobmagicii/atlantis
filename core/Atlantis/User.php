<?php

namespace Atlantis;
use \Atlantis as Atlantis;
use \Nether   as Nether;

use \Exception as Exception;

class User
extends Nether\Auth\User {

	protected
	$Blogs = [];

	public function
	GetBlogs():
	Array {

		return $this->Blogs;
	}

	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////

	public function
	__ready():
	Void {

		$this->Blogs = Atlantis\Blog\User::ListByUserID($this->ID);

		return;
	}

	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////

}
